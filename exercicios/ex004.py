dig = input('Digite algo: ')

print('O tipo primitivo desse valor é', type(dig))
print('so contem espaço?', dig.isspace())
print('É um numero?', dig.isnumeric())
print('É alfabetico?', dig.isalpha())
print('É alfaNumerico?', dig.isalnum())
print('Esta em maiusculo', dig.isupper())
print('Esta em minusculo', dig.islower())
print('Esta capitalizado',dig.istitle())





